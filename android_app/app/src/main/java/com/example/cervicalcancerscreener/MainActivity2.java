    void postRequest(String postUrl, RequestBody postBody) {

        OkHttpClient client = new OkHttpClient();

        Request request = new Request.Builder()
                .url(postUrl)
                .post(postBody)
                .build();

        client.newCall(request).enqueue(new Callback() {
            @Override
            public void onFailure(Call call, IOException e) {
                // Cancel the post on failure.
                progressDialog.dismiss();
                call.cancel();
                Log.i("Messgae", "Connection Failed");
//                Toast.makeText(getApplicationContext(), "failed", Toast.LENGTH_LONG).show();


                // In order to access the TextView inside the UI thread, the code is executed inside runOnUiThread()
                runOnUiThread(new Runnable() {
                    @Override
                    public void run() {
                        classifyButton.setText("Retry");

                    }
                });

            }

            @Override
            public void onResponse(Call call, Response response) throws IOException {
                progressDialog.dismiss();
                // In order to access the TextView inside the UI thread, the code is executed inside runOnUiThread()
                runOnUiThread(new Runnable() {
                    @Override
                    public void run() {
                        try {

                            Log.i("response", "displaying result");
                            String prediction = response.body().string();

//                            JSONObject jsonObject = new JSONObject(prediction);
//                            String classType = jsonObject.getString("class");
//                            String probability = jsonObject.getString("probability");

                            tothirdActivity(prediction);


                        } catch (IOException e) {
                            Log.i("response", "displaying error");
                            e.printStackTrace();
                        }
                    }
                });

            }

        });
    }

